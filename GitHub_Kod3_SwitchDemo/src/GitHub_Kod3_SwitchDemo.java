
public class GitHub_Kod3_SwitchDemo {

	

	public static void main(String[] args) {

		char grade ='A';
		switch(grade) {
		      case 'A':
		    	  System.out.println("Mukemmel");
		    	  break;
		      case 'B':
		    	  System.out.println("İyi");
		    	  break;
		      case 'C':
		    	  System.out.println("Yeterli");
		    	  break;
		      case 'D':
		    	  System.out.println("Koşullu Yeterli");
		    	  break;
		      default:
		    	  System.out.println("Kaldınız");
		    	  break;
		
		
		
		}
	}

}
